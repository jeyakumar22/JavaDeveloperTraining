package javaBasics;

public class Operators {


    public static  void main (String args[]){
        //bitwise
        System.out.println(4<<2);  // add 0 from right
        System.out.println(4>>2);  // shifting to position

        //ternery
        System.out.println(10>20 ? "10 is greater" : "10 is smaller");
    }
}
